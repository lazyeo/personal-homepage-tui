import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = resolve(root, 'src/data/ai-context.md');
const targetPath = resolve(root, 'functions/api/portfolio-context.js');

const context = readFileSync(sourcePath, 'utf8');
const output = `// Auto-generated by scripts/generate-functions-context.mjs. Do not edit by hand.\nexport const PORTFOLIO_CONTEXT = ${JSON.stringify(context)};\n`;

mkdirSync(dirname(targetPath), { recursive: true });
writeFileSync(targetPath, output);
